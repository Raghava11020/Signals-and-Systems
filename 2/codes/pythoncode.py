import matplotlib.pyplot as plt

def x(n):
    return 5 + (8/3) * n

# Generate values for n
n_values = list(range(10))  # Adjust the range as needed

# Calculate corresponding x(n) values
x_values = [x(n) for n in n_values]

# Plot stem graph
plt.stem(n_values, x_values, use_line_collection=True)
plt.xlabel('n')
plt.ylabel('x(n)')
plt.title('Stem Graph of x(n) = 5 + (8/3) * n')
plt.show()
